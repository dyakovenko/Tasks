package QuickHull;
/** Class point **/

public class QPoint {
	
 private int x, y;
	

	public QPoint(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	
	public QPoint() {
		super();
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
}
